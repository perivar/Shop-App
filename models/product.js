class Product {
  constructor(id, userId, seller, name, url, description, price, location, profilePic){
    this.id = id;
    this.userId = userId;
    this.seller = seller
    this.name = name;
    this.url = url;
    this.description = description;
    this.price = price;
    this.location = location;
    this.profilePic = profilePic;
  }
}

export default Product;
